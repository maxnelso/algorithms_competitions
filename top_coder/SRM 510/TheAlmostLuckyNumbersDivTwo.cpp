class TheAlmostLuckyNumbersDivTwo {
  public: int find(int a, int b);
};

bool almost_luck(int a) {
  string s = to_string(a);
  int count = 0;
  for (int i = 0; i < s.size(); ++i) {
    if (s[i] != '7' && s[i] != '4') {
      count++;
    }
  }
  return count <= 1;
}

int TheAlmostLuckyNumbersDivTwo::find(int a, int b) {
  int count = 0;
  for (int i = a; i <= b; ++i) {
    if (almost_luck(i)) {
      count++;
    }
  }
  return count;
}

